(
    ID_PROYECTO INTEGER NOT NULL,
    ID_SUPERVISOR VARCHAR NOT NULL,
    NOM_PROYECTO VARCHAR NOT NULL,
    ID_MUNICIPIO CHAR(5) NOT NULL,
    DESCRIPCION_PROYECTO VARCHAR NOT NULL,
    FECHA_CREACION TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    USUARIO_CREACION VARCHAR NOT NULL,
    FECHA_MODIFICACION TIMESTAMP WITHOUT TIME ZONE,
    USUARIO_MODIFICACION VARCHAR,
    PRIMARY KEY (ID_PROYECTO),
    FOREIGN KEY (ID_SUPERVISOR) REFERENCES TAB_SUPERVISORES(ID_SUPERVISOR),
    FOREIGN KEY (ID_MUNICIPIO) REFERENCES TAB_MUNICIPIOS(ID_MUNICIPIO)
