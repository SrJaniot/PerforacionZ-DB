-- TABLA DE MUNICIPIOS DEPENDIENTE DE DEPARTAMENTOS
--TABLA CIUDADES
CREATE TABLE IF NOT EXISTS TAB_MUNICIPIOS
(
  ID_DPTO        CHAR(2)     NOT NULL,
