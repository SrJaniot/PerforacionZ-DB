-- 1. COMPARATIVA DE MODELOS DE BROCAS (Matriz de Desempeño)
-- ═══════════════════════════════════════════════════════════════════════════
SELECT
UNION ALL
SELECT 
    b.NOM_BROCA AS MODELO,
    'Tipo: ' || b.TIPO_BROCA || ' | Tamaño: ' || b.TAMANIO_BROCA || 'mm | Matriz: ' || b.MATRIX_BROCA AS ESPECIFICACIONES,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA)::VARCHAR || ' piezas' AS INVENTARIO_ACTUAL,
    COUNT(DISTINCT CASE WHEN bi.ESTADO_BROCA = 'EN_USO' THEN bi.ID_BROCA_INSTANCIADA END)::VARCHAR || ' activas' AS EN_OPERACION,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2)::VARCHAR || ' m' AS PROMEDIO_PROFUNDIDAD_TRAMO,
    ROUND((SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) / 
            NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 0)::VARCHAR || ' m/pieza' AS PRODUCTIVIDAD,
    COUNT(DISTINCT mb.ID_PERFORACION)::VARCHAR AS PERFORACIONES_EXITOSAS,
    ROUND((COUNT(DISTINCT mb.ID_MOVIMIENTO)::NUMERIC / NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 1)::VARCHAR AS MOVIMIENTOS_PROMEDIO
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA, b.TIPO_BROCA, b.TAMANIO_BROCA, b.MATRIX_BROCA
ORDER BY 
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) DESC;

-- 2. ANÁLISIS DE COMPATIBILIDAD BROCA-SUELO (Matriz de Compatibilidad)
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'MATRIZ DE COMPATIBILIDAD: BROCAS vs SUELOS (Métrica: Promedio de Profundidad en Tramo)'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    s.TIPO_SUELO AS TIPO_SUELO,
    ROUND(MAX(CASE WHEN b.TIPO_BROCA = 'BROCA ESCALONADA' THEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) END)::NUMERIC, 1)::VARCHAR AS BROCA_ESCALONADA,
    ROUND(MAX(CASE WHEN b.TIPO_BROCA = 'BROCA CÓNICA' THEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) END)::NUMERIC, 1)::VARCHAR AS BROCA_CÓNICA,
    ROUND(MAX(CASE WHEN b.TIPO_BROCA = 'BROCA TRICONO' THEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) END)::NUMERIC, 1)::VARCHAR AS BROCA_TRICONO,
    ROUND(MAX(CASE WHEN b.TIPO_BROCA = 'BROCA PDC' THEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) END)::NUMERIC, 1)::VARCHAR AS BROCA_PDC,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 1)::VARCHAR AS PROMEDIO_GENERAL
FROM 
    TAB_SUELOS s
    LEFT JOIN TAB_TRAMO_SUELO ts ON s.ID_SUELO = ts.ID_SUELO
    LEFT JOIN TAB_PERFORACIONES p ON ts.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
GROUP BY 
    s.ID_SUELO, s.TIPO_SUELO
ORDER BY 
    s.TIPO_SUELO;

-- 3. EFICIENCIA DE PROYECTOS (ROI de Brocas por Proyecto)
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'EFICIENCIA POR PROYECTO (Profundidad vs Recursos Utilizados)'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    pr.NOM_PROYECTO AS PROYECTO,
    m.NOM_MUNICIPIO AS UBICACIÓN,
    COUNT(DISTINCT p.ID_PERFORACION)::VARCHAR AS PERFORACIONES,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA)::VARCHAR AS BROCAS_USADAS,
    ROUND((COUNT(DISTINCT p.ID_PERFORACION)::NUMERIC / 
            NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 2)::VARCHAR AS PERFOR_POR_BROCA,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2)::VARCHAR || ' m' AS TOTAL_PERFORADO,
    ROUND((SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) / 
            NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 0)::VARCHAR || ' m/broca' AS EFICIENCIA,
    EXTRACT(DAY FROM (MAX(p.FECHA_FIN_PERFORACION) - MIN(p.FECHA_INICIO_PERFORACION)))::VARCHAR || ' días' AS DURACIÓN
FROM 
    TAB_PROYECTOS pr
    LEFT JOIN TAB_MUNICIPIOS m ON pr.ID_MUNICIPIO = m.ID_MUNICIPIO
    LEFT JOIN TAB_PERFORACIONES p ON pr.ID_PROYECTO = p.ID_PROYECTO
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    pr.ID_PROYECTO, pr.NOM_PROYECTO, m.NOM_MUNICIPIO
ORDER BY 
    COUNT(DISTINCT p.ID_PERFORACION) DESC;

-- 4. ANÁLISIS TEMPORAL (Tendencias de Uso en el Tiempo)
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'TENDENCIAS OPERACIONALES POR PERÍODO'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    TO_CHAR(DATE_TRUNC('month', mb.FECHA_HORA_MOVIMIENTO), 'YYYY-MM') AS PERÍODO,
    COUNT(DISTINCT mb.ID_MOVIMIENTO)::VARCHAR AS MOVIMIENTOS,
    COUNT(DISTINCT mb.ID_PERFORACION)::VARCHAR AS PERFORACIONES_ACTIVAS,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA)::VARCHAR AS BROCAS_EN_USO,
    ROUND(AVG(CASE WHEN mb.TIPO_MOVIMIENTO = 'SALIDA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END)::NUMERIC, 2)::VARCHAR || ' m' AS PROFUNDIDAD_PROMEDIO
FROM 
    TAB_MOVIMIENTO_DE_BROCA mb
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
GROUP BY 
    DATE_TRUNC('month', mb.FECHA_HORA_MOVIMIENTO)
ORDER BY 
    PERÍODO DESC;

-- 5. ANÁLISIS DE DESGASTE COMPARATIVO (Vida Útil vs Expectativa)
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'ANÁLISIS DE DESGASTE: ESPERADO VS REAL'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    b.NOM_BROCA AS MODELO_BROCA,
    ROUND(AVG(EXTRACT(DAY FROM (bi.FECHA_ÚLTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)))::NUMERIC, 0)::VARCHAR || ' días' AS VIDA_ÚTIL_PROMEDIO,
    ROUND(AVG(COUNT(DISTINCT mb.ID_MOVIMIENTO)) OVER (PARTITION BY b.ID_BROCA)::NUMERIC, 1)::VARCHAR AS MOVIMIENTOS_PROMEDIO,
    ROUND((AVG(COUNT(DISTINCT mb.ID_MOVIMIENTO)) OVER (PARTITION BY b.ID_BROCA) / 
            NULLIF(AVG(EXTRACT(DAY FROM (bi.FECHA_ÚLTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA))), 0))::NUMERIC, 2)::VARCHAR AS MOVIMIENTOS_POR_DÍA,
    ROUND(AVG(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)) OVER (PARTITION BY b.ID_BROCA)::NUMERIC, 0)::VARCHAR || ' m' AS METROS_TOTALES_PROMEDIO,
    CASE 
        WHEN AVG(COUNT(DISTINCT mb.ID_MOVIMIENTO)) OVER (PARTITION BY b.ID_BROCA) > 30 THEN 'ALTO DESGASTE - Revisar frecuencia'
        WHEN AVG(COUNT(DISTINCT mb.ID_MOVIMIENTO)) OVER (PARTITION BY b.ID_BROCA) BETWEEN 15 AND 30 THEN 'DESGASTE NORMAL - Mantenimiento regular'
        ELSE 'BAJO DESGASTE - Óptimo'
    END AS ESTADO_DESGASTE
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
WHERE 
    bi.FECHA_ÚLTIMO_USO_BROCA IS NOT NULL
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA, bi.ID_BROCA_INSTANCIADA, bi.FECHA_REGISTRO_BROCA, bi.FECHA_ÚLTIMO_USO_BROCA
ORDER BY 
    b.NOM_BROCA;

-- 6. REPORTE DE ESPECIALIZACIÓN (Qué broca es mejor para qué)
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'ESPECIALIZACIÓN DE BROCAS: FORTALEZAS POR TIPO'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    b.TIPO_BROCA AS TIPO_BROCA,
    COUNT(DISTINCT b.ID_BROCA)::VARCHAR AS MODELOS,
    COUNT(DISTINCT s.ID_SUELO)::VARCHAR AS SUELOS_PERFORADOS,
    STRING_AGG(DISTINCT s.TIPO_SUELO, ', ' ORDER BY s.TIPO_SUELO)::VARCHAR AS ESPECIALIDAD_EN_SUELOS,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2)::VARCHAR || ' m' AS PROFUNDIDAD_PROMEDIO,
    ROUND((SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) / 
            NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 0)::VARCHAR || ' m' AS PRODUCTIVIDAD_POR_INSTANCIA,
    CASE 
        WHEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) > 50 THEN '🌟 ESPECIALISTA EN SUELOS BLANDOS'
        WHEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) BETWEEN 20 AND 50 THEN '🌟🌟 VERSÁTIL - SUELOS MIXTOS'
        ELSE '🌟🌟🌟 ESPECIALISTA EN SUELOS DUROS'
    END AS ESPECIALIDAD
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
    LEFT JOIN TAB_SUELOS s ON ts.ID_SUELO = s.ID_SUELO
GROUP BY 
    b.TIPO_BROCA
ORDER BY 
    AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) DESC;
