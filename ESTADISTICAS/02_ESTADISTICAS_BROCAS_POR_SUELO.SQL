/*
================================================================================
ESTADÍSTICAS DE BROCAS POR TIPO DE SUELO
================================================================================
Análisis de qué tipo de suelo perfora mejor cada broca:
- Profundidad perforada por tipo de suelo
- Cantidad de tramos por suelo
- Eficiencia por combinación broca-suelo
- Suelos más difíciles de perforar

NOTA: Ejecuta cada query por separado (sin UNION)
================================================================================
*/

-- ═══════════════════════════════════════════════════════════════════════════
-- 1. EFECTIVIDAD DE BROCAS POR TIPO DE SUELO
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    RANK() OVER (PARTITION BY s.TIPO_SUELO ORDER BY SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) DESC) || '. ' ||
    b.NOM_BROCA || ' (Tamaño: ' || b.TAMANIO_BROCA || 'mm, Matriz: ' || b.MATRIX_BROCA || ')' AS BROCA,
    s.NOM_SUELO AS NOMBRE_SUELO,
    s.TIPO_SUELO AS TIPO_SUELO,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS INSTANCIAS_USADAS,
    COUNT(DISTINCT ts.ID_TRAMO) AS TOTAL_TRAMOS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_M,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROMEDIO_PROFUNDIDAD_TRAMO,
    ROUND((SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) / NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 2) AS PROFUNDIDAD_POR_INSTANCIA
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
    LEFT JOIN TAB_SUELOS s ON ts.ID_SUELO = s.ID_SUELO
WHERE 
    s.ID_SUELO IS NOT NULL
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA, b.TAMANIO_BROCA, b.MATRIX_BROCA, 
    s.ID_SUELO, s.NOM_SUELO, s.TIPO_SUELO
ORDER BY 
    s.TIPO_SUELO, PROFUNDIDAD_TOTAL_M DESC;

-- ═══════════════════════════════════════════════════════════════════════════
-- 2. MATRIZ DE BROCAS vs SUELOS (Mejor combinación)
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    b.TIPO_BROCA AS TIPO_BROCA,
    s.TIPO_SUELO AS TIPO_SUELO,
    COUNT(DISTINCT ts.ID_TRAMO) AS TRAMOS_PERFORADOS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_M,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO,
    ROUND((SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) / NULLIF(COUNT(DISTINCT ts.ID_TRAMO), 0))::NUMERIC, 2) AS EFICIENCIA_METROS_POR_TRAMO
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
    LEFT JOIN TAB_SUELOS s ON ts.ID_SUELO = s.ID_SUELO
WHERE 
    s.ID_SUELO IS NOT NULL
GROUP BY 
    b.TIPO_BROCA, s.TIPO_SUELO
ORDER BY 
    b.TIPO_BROCA, PROFUNDIDAD_PROMEDIO DESC;

-- ═══════════════════════════════════════════════════════════════════════════
-- 3. SUELOS MÁS DIFÍCILES DE PERFORAR (Menos profundidad promedio)
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    RANK() OVER (ORDER BY AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) ASC) || '. ' ||
    s.NOM_SUELO || ' (' || s.TIPO_SUELO || ')' AS SUELO,
    s.DESCRIPCION_SUELO AS DESCRIPCION,
    COUNT(DISTINCT ts.ID_TRAMO) AS TOTAL_TRAMOS,
    COUNT(DISTINCT b.ID_BROCA) AS BROCAS_USADAS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_M,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO_TRAMO,
    CASE 
        WHEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) > 50 THEN 'FÁCIL'
        WHEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) BETWEEN 20 AND 50 THEN 'MEDIO'
        WHEN AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) BETWEEN 10 AND 20 THEN 'DIFÍCIL'
        ELSE 'MUY DIFÍCIL'
    END AS DIFICULTAD
FROM 
    TAB_SUELOS s
    LEFT JOIN TAB_TRAMO_SUELO ts ON s.ID_SUELO = ts.ID_SUELO
    LEFT JOIN TAB_PERFORACIONES p ON ts.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
GROUP BY 
    s.ID_SUELO, s.NOM_SUELO, s.TIPO_SUELO, s.DESCRIPCION_SUELO
HAVING 
    COUNT(DISTINCT ts.ID_TRAMO) > 0
ORDER BY 
    PROFUNDIDAD_PROMEDIO_TRAMO ASC;

-- ═══════════════════════════════════════════════════════════════════════════
-- 4. MEJOR BROCA PARA CADA TIPO DE SUELO
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    s.TIPO_SUELO AS TIPO_SUELO,
    b.NOM_BROCA AS MEJOR_BROCA,
    b.TIPO_BROCA AS TIPO,
    'Tamaño: ' || b.TAMANIO_BROCA || 'mm | Matriz: ' || b.MATRIX_BROCA AS ESPECIFICACIONES,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO_M,
    COUNT(DISTINCT ts.ID_TRAMO) AS TRAMOS_PERFORADOS
FROM 
    TAB_SUELOS s
    LEFT JOIN TAB_TRAMO_SUELO ts ON s.ID_SUELO = ts.ID_SUELO
    LEFT JOIN TAB_PERFORACIONES p ON ts.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
WHERE 
    b.ID_BROCA IS NOT NULL
GROUP BY 
    s.TIPO_SUELO, b.ID_BROCA, b.NOM_BROCA, b.TIPO_BROCA, b.TAMANIO_BROCA, b.MATRIX_BROCA
ORDER BY 
    s.TIPO_SUELO, PROFUNDIDAD_PROMEDIO_M DESC;
