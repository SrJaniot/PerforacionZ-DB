/*
================================================================================
ESTADÍSTICAS DE RENDIMIENTO DE BROCAS
================================================================================
Análisis de cuál es la broca que mejor perfora basado en:
- Profundidad total perforada
- Cantidad de tramos perforados
- Promedio de profundidad por uso
- Tiempo en uso (horas)

NOTA: Ejecuta cada consulta por separado (no UNION)
================================================================================
*/

-- ═══════════════════════════════════════════════════════════════════════════
-- 1. TOP 10 BROCAS POR PROFUNDIDAD TOTAL PERFORADA
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    RANK() OVER (ORDER BY SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) DESC) || '. ' ||
    b.NOM_BROCA || 
    ' (Tipo: ' || b.TIPO_BROCA || ', Tamaño: ' || b.TAMANIO_BROCA || 'mm)' AS BROCA,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS CANTIDAD_INSTANCIAS,
    COUNT(DISTINCT ts.ID_TRAMO) AS TOTAL_TRAMOS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_M,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROMEDIO_PROFUNDIDAD_POR_TRAMO,
    ROUND((SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) / NULLIF(COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA), 0))::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO_POR_INSTANCIA
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA, b.TIPO_BROCA, b.TAMANIO_BROCA
HAVING 
    SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO) IS NOT NULL
ORDER BY 
    PROFUNDIDAD_TOTAL_M DESC
LIMIT 10;

-- ═══════════════════════════════════════════════════════════════════════════
-- 2. EFICIENCIA POR BROCA (Profundidad perforada vs cantidad de movimientos)
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    RANK() OVER (ORDER BY 
        ROUND((SUM(CASE WHEN mb.TIPO_MOVIMIENTO = 'SALIDA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END) - 
        SUM(CASE WHEN mb.TIPO_MOVIMIENTO = 'ENTRADA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END))::NUMERIC, 2) DESC) || '. ' ||
    b.NOM_BROCA AS BROCA,
    COUNT(DISTINCT mb.ID_BROCA_INSTANCIADA) AS INSTANCIAS_USADAS,
    COUNT(mb.ID_MOVIMIENTO) AS TOTAL_MOVIMIENTOS,
    COUNT(DISTINCT mb.ID_PERFORACION) AS PERFORACIONES_PERFORADAS,
    ROUND((SUM(CASE WHEN mb.TIPO_MOVIMIENTO = 'SALIDA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END) - 
        SUM(CASE WHEN mb.TIPO_MOVIMIENTO = 'ENTRADA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END))::NUMERIC, 2) AS PROFUNDIDAD_NETA_M,
    ROUND(AVG(ABS(mb.PROFUNDIDAD_MOVIMIENTO))::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO_MOVIMIENTO
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA
HAVING 
    COUNT(DISTINCT mb.ID_BROCA_INSTANCIADA) > 0
ORDER BY 
    PROFUNDIDAD_NETA_M DESC;

-- ═══════════════════════════════════════════════════════════════════════════
-- 3. DURABILIDAD DE INSTANCIAS (Qué instancia de broca ha perforado más)
-- ═══════════════════════════════════════════════════════════════════════════
SELECT 
    RANK() OVER (ORDER BY ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) DESC) || '. ' ||
    bi.ID_BROCA_INSTANCIADA || ' - ' || b.NOM_BROCA AS INSTANCIA_BROCA,
    bi.ESTADO_BROCA AS ESTADO,
    COUNT(DISTINCT mb.ID_PERFORACION) AS PERFORACIONES,
    COUNT(DISTINCT ts.ID_TRAMO) AS TRAMOS_PERFORADOS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_M,
    ROUND(AVG(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO_POR_TRAMO,
    EXTRACT(DAY FROM (bi.FECHA_ULTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)) || ' días' AS TIEMPO_EN_USO
FROM 
    TAB_BROCAINSTANCIADAS bi
    JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
WHERE 
    bi.FECHA_ULTIMO_USO_BROCA IS NOT NULL
GROUP BY 
    bi.ID_BROCA_INSTANCIADA, b.NOM_BROCA, bi.ESTADO_BROCA, bi.FECHA_REGISTRO_BROCA, bi.FECHA_ULTIMO_USO_BROCA
ORDER BY 
    PROFUNDIDAD_PROMEDIO_POR_TRAMO DESC
LIMIT 15;
