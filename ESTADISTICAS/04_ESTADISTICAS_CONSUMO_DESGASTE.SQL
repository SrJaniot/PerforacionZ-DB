/*
================================================================================
ESTADÍSTICAS DE CONSUMO Y DESGASTE DE BROCAS
================================================================================
Análisis del consumo y mantenimiento de brocas:
- Brocas por estado (NUEVA, EN_USO, DAÑADA, DESCARTADA)
- Tiempo de vida útil promedio
- Frecuencia de uso
- Rotación de brocas
- Predicción de mantenimiento
================================================================================
*/

-- ═══════════════════════════════════════════════════════════════════════════
-- 1. ESTADO GENERAL DEL INVENTARIO DE BROCAS
-- ═══════════════════════════════════════════════════════════════════════════
SELECT
UNION ALL
SELECT 
    bi.ESTADO_BROCA AS ESTADO,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS CANTIDAD_INSTANCIAS,
    COUNT(DISTINCT bi.ID_BROCA) AS MODELOS_DISTINTOS,
    COUNT(DISTINCT mb.ID_PERFORACION) AS PERFORACIONES_REALIZADAS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_ACUMULADA,
    ROUND(AVG(EXTRACT(DAY FROM (bi.FECHA_ULTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)))::NUMERIC, 0) || ' días' AS PROMEDIO_TIEMPO_ACTIVA
FROM 
    TAB_BROCAINSTANCIADAS bi
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    bi.ESTADO_BROCA
ORDER BY 
    CANTIDAD_INSTANCIAS DESC;

-- 2. VIDA ÚTIL DE BROCAS POR MODELO
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'VIDA ÚTIL PROMEDIO DE BROCAS POR MODELO'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    b.NOM_BROCA AS MODELO_BROCA,
    b.TIPO_BROCA AS TIPO,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS INSTANCIAS_TOTALES,
    COUNT(DISTINCT CASE WHEN bi.ESTADO_BROCA = 'EN_USO' THEN bi.ID_BROCA_INSTANCIADA END) AS EN_USO,
    COUNT(DISTINCT CASE WHEN bi.ESTADO_BROCA = 'DAÑADA' THEN bi.ID_BROCA_INSTANCIADA END) AS DAÑADAS,
    COUNT(DISTINCT CASE WHEN bi.ESTADO_BROCA = 'DESCARTADA' THEN bi.ID_BROCA_INSTANCIADA END) AS DESCARTADAS,
    ROUND(AVG(EXTRACT(DAY FROM (bi.FECHA_ULTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)))::NUMERIC, 0) || ' días' AS PROMEDIO_VIDA_ÚTIL,
    ROUND(AVG(ABS(mb.PROFUNDIDAD_MOVIMIENTO))::NUMERIC, 2) || ' m' AS PROMEDIO_PROFUNDIDAD_MOVIMIENTO,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) || ' m' AS TOTAL_METROS_PERFORADOS
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA, b.TIPO_BROCA
ORDER BY 
    PROMEDIO_VIDA_ÚTIL DESC;

-- 3. ROTACIÓN Y FRECUENCIA DE USO DE BROCAS
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'ROTACIÓN Y FRECUENCIA DE USO DE BROCAS'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    bi.ID_BROCA_INSTANCIADA AS SERIE,
    b.NOM_BROCA AS BROCA,
    bi.ESTADO_BROCA AS ESTADO,
    COUNT(DISTINCT mb.ID_PERFORACION) AS PERFORACIONES,
    COUNT(DISTINCT mb.ID_MOVIMIENTO) AS TOTAL_MOVIMIENTOS,
    COUNT(DISTINCT CASE WHEN mb.TIPO_MOVIMIENTO = 'ENTRADA' THEN mb.ID_MOVIMIENTO END) AS ENTRADAS,
    COUNT(DISTINCT CASE WHEN mb.TIPO_MOVIMIENTO = 'SALIDA' THEN mb.ID_MOVIMIENTO END) AS SALIDAS,
    ROUND((SUM(CASE WHEN mb.TIPO_MOVIMIENTO = 'SALIDA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END) -
           SUM(CASE WHEN mb.TIPO_MOVIMIENTO = 'ENTRADA' THEN mb.PROFUNDIDAD_MOVIMIENTO ELSE 0 END))::NUMERIC, 2) AS PROFUNDIDAD_NETA_M,
    EXTRACT(DAY FROM (bi.FECHA_ULTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)) || ' días' AS DÍAS_EN_USO,
    CASE 
        WHEN COUNT(DISTINCT mb.ID_MOVIMIENTO) > 20 THEN 'ALTA'
        WHEN COUNT(DISTINCT mb.ID_MOVIMIENTO) BETWEEN 10 AND 20 THEN 'MEDIA'
        WHEN COUNT(DISTINCT mb.ID_MOVIMIENTO) > 0 THEN 'BAJA'
        ELSE 'NO USADO'
    END AS FRECUENCIA_USO
FROM 
    TAB_BROCAINSTANCIADAS bi
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
GROUP BY 
    bi.ID_BROCA_INSTANCIADA, b.NOM_BROCA, bi.ESTADO_BROCA, 
    bi.FECHA_REGISTRO_BROCA, bi.FECHA_ULTIMO_USO_BROCA
ORDER BY 
    COUNT(DISTINCT mb.ID_MOVIMIENTO) DESC;

-- 4. ÍNDICE DE DESGASTE Y MANTENIMIENTO
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'ÍNDICE DE DESGASTE Y NECESIDAD DE MANTENIMIENTO'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    RANK() OVER (ORDER BY 
        (COUNT(DISTINCT mb.ID_MOVIMIENTO)::NUMERIC / NULLIF(EXTRACT(DAY FROM (bi.FECHA_ULTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)), 0)) DESC) || '. ' ||
    bi.ID_BROCA_INSTANCIADA || ' - ' || b.NOM_BROCA AS SERIE_BROCA,
    bi.ESTADO_BROCA AS ESTADO_ACTUAL,
    COUNT(DISTINCT mb.ID_MOVIMIENTO) AS USO_TOTAL,
    ROUND(EXTRACT(DAY FROM (bi.FECHA_ULTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA))::NUMERIC, 0) AS DÍAS_ACTIVA,
    ROUND((COUNT(DISTINCT mb.ID_MOVIMIENTO)::NUMERIC / NULLIF(EXTRACT(DAY FROM (bi.FECHA_ÚLTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)), 0))::NUMERIC, 2) AS MOVIMIENTOS_POR_DÍA,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS METROS_PERFORADOS,
    CASE 
        WHEN bi.ESTADO_BROCA = 'DAÑADA' OR bi.ESTADO_BROCA = 'DESCARTADA' THEN 'REQUIERE REEMPLAZO'
        WHEN COUNT(DISTINCT mb.ID_MOVIMIENTO) > 30 AND ROUND((COUNT(DISTINCT mb.ID_MOVIMIENTO)::NUMERIC / NULLIF(EXTRACT(DAY FROM (bi.FECHA_ÚLTIMO_USO_BROCA - bi.FECHA_REGISTRO_BROCA)), 0))::NUMERIC, 2) > 0.5 THEN 'MANTENIMIENTO INMINENTE'
        WHEN COUNT(DISTINCT mb.ID_MOVIMIENTO) > 20 THEN 'REVISAR EN PRÓXIMO MANTENIMIENTO'
        ELSE 'NORMAL'
    END AS RECOMENDACIÓN_MANTENIMIENTO
FROM 
    TAB_BROCAINSTANCIADAS bi
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
WHERE 
    bi.FECHA_ÚLTIMO_USO_BROCA IS NOT NULL
GROUP BY 
    bi.ID_BROCA_INSTANCIADA, b.NOM_BROCA, bi.ESTADO_BROCA, 
    bi.FECHA_REGISTRO_BROCA, bi.FECHA_ÚLTIMO_USO_BROCA
ORDER BY 
    MOVIMIENTOS_POR_DÍA DESC;

-- 5. ANÁLISIS DE COSTOS Y ROI POR BROCA
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'ANÁLISIS DE PRODUCTIVIDAD (METROS PERFORADOS VS USO)'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    RANK() OVER (ORDER BY 
        (ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) / NULLIF(COUNT(DISTINCT mb.ID_MOVIMIENTO), 0)) DESC) || '. ' ||
    bi.ID_BROCA_INSTANCIADA || ' - ' || b.NOM_BROCA AS BROCA,
    b.TIPO_BROCA || ' (' || b.TAMANIO_BROCA || 'mm)' AS ESPECIFICACIONES,
    COUNT(DISTINCT mb.ID_MOVIMIENTO) AS MOVIMIENTOS_TOTALES,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS METROS_PERFORADOS,
    ROUND((ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) / NULLIF(COUNT(DISTINCT mb.ID_MOVIMIENTO), 0))::NUMERIC, 2) AS METROS_POR_MOVIMIENTO,
    ROUND((ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) / NULLIF(COUNT(DISTINCT mb.ID_PERFORACION), 0))::NUMERIC, 2) AS METROS_POR_PERFORACIÓN
FROM 
    TAB_BROCAINSTANCIADAS bi
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
WHERE 
    COUNT(DISTINCT mb.ID_MOVIMIENTO) > 0
GROUP BY 
    bi.ID_BROCA_INSTANCIADA, b.NOM_BROCA, b.TIPO_BROCA, b.TAMANIO_BROCA
ORDER BY 
    METROS_POR_MOVIMIENTO DESC;
