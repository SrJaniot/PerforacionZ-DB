/*
================================================================================
ESTADÍSTICAS DE BROCAS POR LOCALIDADES/MUNICIPIOS
================================================================================
Análisis de dónde se usan más los diferentes tipos de brocas:
- Distribución geográfica de uso de brocas
- Brocas más usadas por municipio
- Departamentos con mayor actividad de perforación
- Proyectos por municipio
================================================================================
*/

-- ═══════════════════════════════════════════════════════════════════════════
-- 1. DISTRIBUCIÓN DE USO DE BROCAS POR MUNICIPIO
-- ═══════════════════════════════════════════════════════════════════════════
SELECT
UNION ALL
SELECT 
    RANK() OVER (PARTITION BY m.NOM_MUNICIPIO ORDER BY COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) DESC) || '. ' ||
    b.NOM_BROCA || ' (Tipo: ' || b.TIPO_BROCA || ')' AS BROCA,
    m.NOM_MUNICIPIO AS MUNICIPIO,
    d.NOM_DPTO AS DEPARTAMENTO,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS INSTANCIAS_USADAS,
    COUNT(DISTINCT p.ID_PERFORACION) AS PERFORACIONES,
    COUNT(DISTINCT mb.ID_MOVIMIENTO) AS MOVIMIENTOS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_M
FROM 
    TAB_BROCAS b
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON b.ID_BROCA = bi.ID_BROCA
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON bi.ID_BROCA_INSTANCIADA = mb.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_PERFORACIONES p ON mb.ID_PERFORACION = p.ID_PERFORACION
    LEFT JOIN TAB_PROYECTOS pr ON p.ID_PROYECTO = pr.ID_PROYECTO
    LEFT JOIN TAB_MUNICIPIOS m ON pr.ID_MUNICIPIO = m.ID_MUNICIPIO
    LEFT JOIN TAB_DPTOS d ON m.ID_DPTO = d.ID_DPTO
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
WHERE 
    m.NOM_MUNICIPIO IS NOT NULL
GROUP BY 
    b.ID_BROCA, b.NOM_BROCA, b.TIPO_BROCA,
    m.ID_MUNICIPIO, m.NOM_MUNICIPIO,
    d.NOM_DPTO
ORDER BY 
    m.NOM_MUNICIPIO, COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) DESC;

-- 2. TOP MUNICIPIOS POR ACTIVIDAD DE PERFORACIÓN
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'TOP MUNICIPIOS POR ACTIVIDAD DE PERFORACIÓN'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    RANK() OVER (ORDER BY COUNT(DISTINCT p.ID_PERFORACION) DESC) || '. ' ||
    m.NOM_MUNICIPIO || ' (' || d.NOM_DPTO || ')' AS MUNICIPIO_DPTO,
    COUNT(DISTINCT pr.ID_PROYECTO) AS PROYECTOS,
    COUNT(DISTINCT p.ID_PERFORACION) AS TOTAL_PERFORACIONES,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS BROCAS_DISTINTAS_USADAS,
    COUNT(DISTINCT b.ID_BROCA) AS MODELOS_BROCAS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS PROFUNDIDAD_TOTAL_PERFORADA_M,
    ROUND(AVG(p.PROFUNDIDAD_ACTUAL)::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO_PERFORACIÓN
FROM 
    TAB_MUNICIPIOS m
    LEFT JOIN TAB_DPTOS d ON m.ID_DPTO = d.ID_DPTO
    LEFT JOIN TAB_PROYECTOS pr ON m.ID_MUNICIPIO = pr.ID_MUNICIPIO
    LEFT JOIN TAB_PERFORACIONES p ON pr.ID_PROYECTO = p.ID_PROYECTO
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    m.ID_MUNICIPIO, m.NOM_MUNICIPIO, d.NOM_DPTO
HAVING 
    COUNT(DISTINCT p.ID_PERFORACION) > 0
ORDER BY 
    TOTAL_PERFORACIONES DESC;

-- 3. PREFERENCIA DE BROCAS POR DEPARTAMENTO
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'PREFERENCIA DE TIPOS DE BROCA POR DEPARTAMENTO'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    d.NOM_DPTO AS DEPARTAMENTO,
    b.TIPO_BROCA AS TIPO_BROCA_PREFERIDA,
    COUNT(DISTINCT b.ID_BROCA) AS MODELOS_DISPONIBLES,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS INSTANCIAS_EN_USO,
    COUNT(DISTINCT p.ID_PERFORACION) AS PERFORACIONES_REALIZADAS,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS METROS_PERFORADOS,
    ROUND((COUNT(DISTINCT p.ID_PERFORACION)::NUMERIC / NULLIF(COUNT(DISTINCT pr.ID_PROYECTO), 0)), 2) AS PROMEDIO_PERFORACIONES_POR_PROYECTO
FROM 
    TAB_DPTOS d
    LEFT JOIN TAB_MUNICIPIOS m ON d.ID_DPTO = m.ID_DPTO
    LEFT JOIN TAB_PROYECTOS pr ON m.ID_MUNICIPIO = pr.ID_MUNICIPIO
    LEFT JOIN TAB_PERFORACIONES p ON pr.ID_PROYECTO = p.ID_PROYECTO
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_BROCAS b ON bi.ID_BROCA = b.ID_BROCA
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
WHERE 
    b.TIPO_BROCA IS NOT NULL
GROUP BY 
    d.ID_DPTO, d.NOM_DPTO, b.TIPO_BROCA
ORDER BY 
    d.NOM_DPTO, COUNT(DISTINCT p.ID_PERFORACION) DESC;

-- 4. PROYECTOS POR MUNICIPIO CON ESTADÍSTICAS DE PERFORACIÓN
SELECT 
    ''
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    'ESTADÍSTICAS DE PROYECTOS POR MUNICIPIO'
UNION ALL
SELECT 
    '═══════════════════════════════════════════════════════════════════'
UNION ALL
SELECT 
    ''
UNION ALL
SELECT 
    pr.NOM_PROYECTO AS PROYECTO,
    m.NOM_MUNICIPIO AS MUNICIPIO,
    COUNT(DISTINCT p.ID_PERFORACION) AS PERFORACIONES,
    COUNT(DISTINCT bi.ID_BROCA_INSTANCIADA) AS BROCAS_USADAS,
    ROUND(MAX(p.PROFUNDIDAD_ACTUAL)::NUMERIC, 2) AS PROFUNDIDAD_MÁXIMA,
    ROUND(AVG(p.PROFUNDIDAD_ACTUAL)::NUMERIC, 2) AS PROFUNDIDAD_PROMEDIO,
    ROUND(SUM(ts.PROFUNDIDAD_FIN - ts.PROFUNDIDAD_INICIO)::NUMERIC, 2) AS TOTAL_TRAMOS_PERFORADOS,
    EXTRACT(DAY FROM (MAX(p.FECHA_FIN_PERFORACION) - MIN(p.FECHA_INICIO_PERFORACION))) || ' días' AS DURACIÓN_PROYECTO
FROM 
    TAB_PROYECTOS pr
    LEFT JOIN TAB_MUNICIPIOS m ON pr.ID_MUNICIPIO = m.ID_MUNICIPIO
    LEFT JOIN TAB_PERFORACIONES p ON pr.ID_PROYECTO = p.ID_PROYECTO
    LEFT JOIN TAB_MOVIMIENTO_DE_BROCA mb ON p.ID_PERFORACION = mb.ID_PERFORACION
    LEFT JOIN TAB_BROCAINSTANCIADAS bi ON mb.ID_BROCA_INSTANCIADA = bi.ID_BROCA_INSTANCIADA
    LEFT JOIN TAB_TRAMO_SUELO ts ON p.ID_PERFORACION = ts.ID_PERFORACION
GROUP BY 
    pr.ID_PROYECTO, pr.NOM_PROYECTO, m.NOM_MUNICIPIO
ORDER BY 
    m.NOM_MUNICIPIO, COUNT(DISTINCT p.ID_PERFORACION) DESC;
