            RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Movimiento insertado exitosamente', 'ID', jID_MOVIMIENTO);
        ELSE
            RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar el movimiento');
        END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;

