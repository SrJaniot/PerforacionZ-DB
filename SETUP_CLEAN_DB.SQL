$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
    jID_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.ID_MOVIMIENTO%TYPE;
BEGIN
    jVALIDACION := FUN_VALIDAR_MOVIMIENTO_DE_BROCA(jID_BROCA_INSTANCIADA, jID_PERFORACION, jFECHA_HORA_MOVIMIENTO, jTIPO_MOVIMIENTO, jPROFUNDIDAD_MOVIMIENTO);

    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        jID_MOVIMIENTO := funcion_Retorna_ultmoid('TAB_MOVIMIENTO_DE_BROCA', 'ID_MOVIMIENTO');
        INSERT INTO TAB_MOVIMIENTO_DE_BROCA (ID_MOVIMIENTO, ID_BROCA_INSTANCIADA, ID_PERFORACION, FECHA_HORA_MOVIMIENTO, TIPO_MOVIMIENTO, PROFUNDIDAD_MOVIMIENTO, USUARIO_CREACION)
        VALUES (jID_MOVIMIENTO, jID_BROCA_INSTANCIADA, jID_PERFORACION, jFECHA_HORA_MOVIMIENTO, jTIPO_MOVIMIENTO, jPROFUNDIDAD_MOVIMIENTO, jUSUARIO_CREACION);

        --ACTUALIZAR LA PROFUNDIDAD_ACTUAL EN TAB_PERFORACIONES
        UPDATE TAB_PERFORACIONES
        SET PROFUNDIDAD_ACTUAL = jPROFUNDIDAD_MOVIMIENTO
        WHERE ID_PERFORACION = jID_PERFORACION;

