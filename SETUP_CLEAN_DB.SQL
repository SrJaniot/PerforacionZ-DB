    jID_BROCA TAB_BROCAINSTANCIADAS.ID_BROCA%TYPE,
    jESTADO_BROCA TAB_BROCAINSTANCIADAS.ESTADO_BROCA%TYPE,
    jFECHA_ULTIMO_USO_BROCA TAB_BROCAINSTANCIADAS.FECHA_ULTIMO_USO_BROCA%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    vESTADOS CONSTANT VARCHAR[] := ARRAY['NUEVA', 'EN_USO', 'DESGASTADA', 'DESCARTADA'];
    vESTADO_ACTUAL TAB_BROCAINSTANCIADAS.ESTADO_BROCA%TYPE;
    vID_BROCA_ACTUAL TAB_BROCAINSTANCIADAS.ID_BROCA%TYPE;
    vFECHA_REGISTRO TAB_BROCAINSTANCIADAS.FECHA_REGISTRO_BROCA%TYPE;
    vTRANSICION_VALIDA BOOLEAN := FALSE;
BEGIN
    IF jID_BROCA_INSTANCIADA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no puede ser nulo');
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAINSTANCIADAS WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no existe en la tabla');
        RETURN RETORNO;
    END IF;

    SELECT ESTADO_BROCA, ID_BROCA, FECHA_REGISTRO_BROCA INTO vESTADO_ACTUAL, vID_BROCA_ACTUAL, vFECHA_REGISTRO
    FROM TAB_BROCAINSTANCIADAS WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA;

    IF jID_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no puede ser nulo');
        RETURN RETORNO;
    ELSIF vID_BROCA_ACTUAL != jID_BROCA THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no puede ser modificado: es inmutable');
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAS WHERE ID_BROCA = jID_BROCA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no existe en TAB_BROCAS');
        RETURN RETORNO;
    ELSIF jESTADO_BROCA IS NULL OR jESTADO_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ESTADO_BROCA no puede ser nulo o vacío');
        RETURN RETORNO;
    ELSIF NOT jESTADO_BROCA = ANY(vESTADOS) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ESTADO_BROCA debe ser: NUEVA, EN_USO, DESGASTADA o DESCARTADA');
        RETURN RETORNO;
    END IF;

    -- Validación de transición de estado
    vTRANSICION_VALIDA := (vESTADO_ACTUAL = jESTADO_BROCA) OR
        (vESTADO_ACTUAL = 'NUEVA' AND jESTADO_BROCA = 'EN_USO') OR
        (vESTADO_ACTUAL = 'EN_USO' AND jESTADO_BROCA = 'DESGASTADA') OR
        (vESTADO_ACTUAL = 'DESGASTADA' AND jESTADO_BROCA = 'DESCARTADA');

    IF NOT vTRANSICION_VALIDA THEN
        RETORNO := json_build_object(
            'CODIGO', 400, 
            'MENSAJE', 'Transición de estado inválida: de ' || vESTADO_ACTUAL || ' a ' || jESTADO_BROCA || '. Progresión válida: NUEVA → EN_USO → DESGASTADA → DESCARTADA'
        );
        RETURN RETORNO;
    END IF;

    IF jFECHA_ULTIMO_USO_BROCA IS NOT NULL AND jFECHA_ULTIMO_USO_BROCA < vFECHA_REGISTRO THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_ULTIMO_USO_BROCA no puede ser menor que FECHA_REGISTRO_BROCA');
        RETURN RETORNO;
    END IF;

    RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;

