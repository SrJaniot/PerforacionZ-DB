-- FUNCION PARA INSERTAR UNA NUEVA BROCA EN LA TABLA DE BROCAS
CREATE OR REPLACE FUNCTION FUN_INSERTAR_BROCAS_JSON(
    jNOM_BROCA TAB_BROCAS.NOM_BROCA%TYPE,
    jTIPO_BROCA TAB_BROCAS.TIPO_BROCA%TYPE,
    jDESCRIPCION_BROCA TAB_BROCAS.DESCRIPCION_BROCA%TYPE,
    jTAMANIO_BROCA TAB_BROCAS.TAMANIO_BROCA%TYPE,
    jMATRIX_BROCA TAB_BROCAS.MATRIX_BROCA%TYPE,
    jUSUARIO_CREACION TAB_BROCAS.USUARIO_CREACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
    jID_BROCA INTEGER;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_BROCAS(jNOM_BROCA, jTIPO_BROCA, jDESCRIPCION_BROCA, jTAMANIO_BROCA, jMATRIX_BROCA);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --GENERAR EL ID DE LA BROCA
    jID_BROCA := funcion_Retorna_ultmoid('TAB_BROCAS', 'ID_BROCA');

    --INSERTAR LA NUEVA BROCA EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        INSERT INTO TAB_BROCAS (ID_BROCA, NOM_BROCA, TIPO_BROCA, DESCRIPCION_BROCA, TAMANIO_BROCA, MATRIX_BROCA, USUARIO_CREACION)
        VALUES (jID_BROCA, jNOM_BROCA, jTIPO_BROCA, jDESCRIPCION_BROCA, jTAMANIO_BROCA, jMATRIX_BROCA, jUSUARIO_CREACION);

        --VERIFICAR SI LA INSERCION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Broca insertada exitosamente', 'ID_BROCA', jID_BROCA); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar la broca'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA ACTUALIZAR LOS DATOS DE UNA BROCA EN LA TABLA DE BROCAS
CREATE OR REPLACE FUNCTION FUN_ACTUALIZAR_BROCAS_JSON(
    jID_BROCA TAB_BROCAS.ID_BROCA%TYPE,
    jNOM_BROCA TAB_BROCAS.NOM_BROCA%TYPE,
    jTIPO_BROCA TAB_BROCAS.TIPO_BROCA%TYPE,
    jDESCRIPCION_BROCA TAB_BROCAS.DESCRIPCION_BROCA%TYPE,
    jTAMANIO_BROCA TAB_BROCAS.TAMANIO_BROCA%TYPE,
    jMATRIX_BROCA TAB_BROCAS.MATRIX_BROCA%TYPE,
    jUSUARIO_MODIFICACION TAB_BROCAS.USUARIO_MODIFICACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_ACTUALIZAR_BROCAS(jID_BROCA, jNOM_BROCA, jTIPO_BROCA, jDESCRIPCION_BROCA, jTAMANIO_BROCA, jMATRIX_BROCA);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --ACTUALIZAR LOS DATOS DE LA BROCA EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_BROCAS
        SET NOM_BROCA = jNOM_BROCA,
            TIPO_BROCA = jTIPO_BROCA,
            DESCRIPCION_BROCA = jDESCRIPCION_BROCA,
            TAMANIO_BROCA = jTAMANIO_BROCA,
            MATRIX_BROCA = jMATRIX_BROCA,
            USUARIO_MODIFICACION = jUSUARIO_MODIFICACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_BROCA = jID_BROCA;

        --VERIFICAR SI LA ACTUALIZACION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Broca actualizada exitosamente'); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar la broca'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE UNA BROCA POR SU ID
CREATE OR REPLACE FUNCTION FUN_OBTENER_BROCA_POR_ID_JSON(
    jID_BROCA TAB_BROCAS.ID_BROCA%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    DATOS_BROCA JSON;
BEGIN
    SELECT json_build_object(
        'ID_BROCA', ID_BROCA,
        'NOM_BROCA', NOM_BROCA,
        'TIPO_BROCA', TIPO_BROCA,
        'DESCRIPCION_BROCA', DESCRIPCION_BROCA,
        'TAMANIO_BROCA', TAMANIO_BROCA,
        'MATRIX_BROCA', MATRIX_BROCA
    )
    INTO DATOS_BROCA
    FROM TAB_BROCAS
    WHERE ID_BROCA = jID_BROCA;

    IF DATOS_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'Broca no encontrada'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Broca encontrada', 'DATOS', DATOS_BROCA); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE TODAS LAS BROCAS
CREATE OR REPLACE FUNCTION FUN_OBTENER_TODAS_BROCAS_JSON() RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    LISTA_BROCAS JSON;
BEGIN
    SELECT json_agg(json_build_object(
        'ID_BROCA', ID_BROCA,
        'NOM_BROCA', NOM_BROCA,
        'TIPO_BROCA', TIPO_BROCA,
        'DESCRIPCION_BROCA', DESCRIPCION_BROCA,
        'TAMANIO_BROCA', TAMANIO_BROCA,
        'MATRIX_BROCA', MATRIX_BROCA
    )) INTO LISTA_BROCAS
    FROM TAB_BROCAS;

    IF LISTA_BROCAS IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'No se encontraron brocas'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Brocas encontradas', 'DATOS', LISTA_BROCAS); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;
