-- FUNCIONES para TAB_BROCAS
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE BROCAS
CREATE OR REPLACE FUNCTION FUN_VALIDAR_BROCAS(
    jNOM_BROCA TAB_BROCAS.NOM_BROCA%TYPE,
    jTIPO_BROCA TAB_BROCAS.TIPO_BROCA%TYPE,
    jDESCRIPCION_BROCA TAB_BROCAS.DESCRIPCION_BROCA%TYPE,
    jTAMANIO_BROCA TAB_BROCAS.TAMANIO_BROCA%TYPE,
    jMATRIX_BROCA TAB_BROCAS.MATRIX_BROCA%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
BEGIN
    --VALIDACION VARIABLE NOM_BROCA
    IF jNOM_BROCA IS NULL OR jNOM_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'NOM_BROCA no puede ser nulo o vacío'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE TIPO_BROCA
    ELSIF jTIPO_BROCA IS NULL OR jTIPO_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'TIPO_BROCA no puede ser nulo o vacío'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE DESCRIPCION_BROCA
    ELSIF jDESCRIPCION_BROCA IS NULL OR jDESCRIPCION_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'DESCRIPCION_BROCA no puede ser nulo o vacío'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE TAMANIO_BROCA
    ELSIF jTAMANIO_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'TAMANIO_BROCA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jTAMANIO_BROCA <= 0 THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'TAMANIO_BROCA debe ser mayor a cero'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE MATRIX_BROCA
    ELSIF jMATRIX_BROCA IS NULL OR jMATRIX_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'MATRIX_BROCA no puede ser nulo o vacío'); 
        RETURN RETORNO;

    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA VALIDAR LOS DATOS A ACTUALIZAR EN LA TABLA DE BROCAS
