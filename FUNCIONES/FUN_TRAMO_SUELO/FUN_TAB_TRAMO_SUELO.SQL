-- FUNCIONES para TAB_TRAMO_SUELO
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE TRAMO_SUELO
CREATE OR REPLACE FUNCTION FUN_VALIDAR_TRAMO_SUELO(
    jID_PERFORACION TAB_TRAMO_SUELO.ID_PERFORACION%TYPE,
    jID_SUELO TAB_TRAMO_SUELO.ID_SUELO%TYPE,
    jPROFUNDIDAD_INICIO TAB_TRAMO_SUELO.PROFUNDIDAD_INICIO%TYPE,
    jPROFUNDIDAD_FIN TAB_TRAMO_SUELO.PROFUNDIDAD_FIN%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    vULTIMA_PROFUNDIDAD_FIN FLOAT;
BEGIN
    --VALIDACION VARIABLE ID_PERFORACION
    IF jID_PERFORACION IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PERFORACION no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_PERFORACIONES WHERE ID_PERFORACION = jID_PERFORACION) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PERFORACION no existe en TAB_PERFORACIONES'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ID_SUELO
    ELSIF jID_SUELO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_SUELO no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_SUELOS WHERE ID_SUELO = jID_SUELO) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_SUELO no existe en TAB_SUELOS'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE PROFUNDIDAD_INICIO
    ELSIF jPROFUNDIDAD_INICIO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_INICIO no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jPROFUNDIDAD_INICIO < 0 THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_INICIO no puede ser negativa'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE PROFUNDIDAD_FIN
    ELSIF jPROFUNDIDAD_FIN IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_FIN no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jPROFUNDIDAD_FIN < 0 THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_FIN no puede ser negativa'); 
        RETURN RETORNO;
    ELSIF jPROFUNDIDAD_FIN <= jPROFUNDIDAD_INICIO THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_FIN debe ser mayor que PROFUNDIDAD_INICIO'); 
        RETURN RETORNO;

    --VALIDACION DE PROFUNDIDAD PROGRESIVA: El PROFUNDIDAD_INICIO debe ser >= a la última PROFUNDIDAD_FIN de la misma perforación
    ELSE
        SELECT MAX(PROFUNDIDAD_FIN) INTO vULTIMA_PROFUNDIDAD_FIN 
        FROM TAB_TRAMO_SUELO 
        WHERE ID_PERFORACION = jID_PERFORACION;

        IF vULTIMA_PROFUNDIDAD_FIN IS NOT NULL AND jPROFUNDIDAD_INICIO < vULTIMA_PROFUNDIDAD_FIN THEN
            RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_INICIO debe ser mayor o igual a la última profundidad (' || vULTIMA_PROFUNDIDAD_FIN || ') registrada para esta perforación'); 
            RETURN RETORNO;
        ELSE
            RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
        END IF;
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


