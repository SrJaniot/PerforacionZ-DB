-- FUNCION PARA INSERTAR UN NUEVO TRAMO SUELO EN LA TABLA
CREATE OR REPLACE FUNCTION FUN_INSERTAR_TRAMO_SUELO_JSON(
    jID_PERFORACION TAB_TRAMO_SUELO.ID_PERFORACION%TYPE,
    jID_SUELO TAB_TRAMO_SUELO.ID_SUELO%TYPE,
    jPROFUNDIDAD_INICIO TAB_TRAMO_SUELO.PROFUNDIDAD_INICIO%TYPE,
    jPROFUNDIDAD_FIN TAB_TRAMO_SUELO.PROFUNDIDAD_FIN%TYPE,
    jUSUARIO_CREACION TAB_TRAMO_SUELO.USUARIO_CREACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
    jID_TRAMO INTEGER;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_TRAMO_SUELO(jID_PERFORACION, jID_SUELO, jPROFUNDIDAD_INICIO, jPROFUNDIDAD_FIN);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --GENERAR EL ID DEL TRAMO
    jID_TRAMO := funcion_Retorna_ultmoid('TAB_TRAMO_SUELO', 'ID_TRAMO');

    --INSERTAR EL NUEVO TRAMO EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        INSERT INTO TAB_TRAMO_SUELO (ID_TRAMO, ID_PERFORACION, ID_SUELO, PROFUNDIDAD_INICIO, PROFUNDIDAD_FIN, USUARIO_CREACION)
        VALUES (jID_TRAMO, jID_PERFORACION, jID_SUELO, jPROFUNDIDAD_INICIO, jPROFUNDIDAD_FIN, jUSUARIO_CREACION);

        --VERIFICAR SI LA INSERCION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Tramo suelo insertado exitosamente', 'ID_TRAMO', jID_TRAMO); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar el tramo suelo'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA ACTUALIZAR LOS DATOS DE UN TRAMO SUELO EN LA TABLA
CREATE OR REPLACE FUNCTION FUN_ACTUALIZAR_TRAMO_SUELO_JSON(
    jID_TRAMO TAB_TRAMO_SUELO.ID_TRAMO%TYPE,
    jID_PERFORACION TAB_TRAMO_SUELO.ID_PERFORACION%TYPE,
    jID_SUELO TAB_TRAMO_SUELO.ID_SUELO%TYPE,
    jPROFUNDIDAD_INICIO TAB_TRAMO_SUELO.PROFUNDIDAD_INICIO%TYPE,
    jPROFUNDIDAD_FIN TAB_TRAMO_SUELO.PROFUNDIDAD_FIN%TYPE,
    jUSUARIO_MODIFICACION TAB_TRAMO_SUELO.USUARIO_MODIFICACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_ACTUALIZAR_TRAMO_SUELO(jID_TRAMO, jID_PERFORACION, jID_SUELO, jPROFUNDIDAD_INICIO, jPROFUNDIDAD_FIN);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --ACTUALIZAR LOS DATOS DEL TRAMO EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_TRAMO_SUELO
        SET ID_PERFORACION = jID_PERFORACION,
            ID_SUELO = jID_SUELO,
            PROFUNDIDAD_INICIO = jPROFUNDIDAD_INICIO,
            PROFUNDIDAD_FIN = jPROFUNDIDAD_FIN,
            USUARIO_MODIFICACION = jUSUARIO_MODIFICACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_TRAMO = jID_TRAMO;

        --VERIFICAR SI LA ACTUALIZACION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Tramo suelo actualizado exitosamente'); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar el tramo suelo'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE UN TRAMO SUELO POR SU ID
CREATE OR REPLACE FUNCTION FUN_OBTENER_TRAMO_SUELO_POR_ID_JSON(
    jID_TRAMO TAB_TRAMO_SUELO.ID_TRAMO%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    DATOS_TRAMO JSON;
BEGIN
    SELECT json_build_object(
        'ID_TRAMO', ID_TRAMO,
        'ID_PERFORACION', ID_PERFORACION,
        'ID_SUELO', ID_SUELO,
        'PROFUNDIDAD_INICIO', PROFUNDIDAD_INICIO,
        'PROFUNDIDAD_FIN', PROFUNDIDAD_FIN
    )
    INTO DATOS_TRAMO
    FROM TAB_TRAMO_SUELO
    WHERE ID_TRAMO = jID_TRAMO;

    IF DATOS_TRAMO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'Tramo suelo no encontrado'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Tramo suelo encontrado', 'DATOS', DATOS_TRAMO); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


