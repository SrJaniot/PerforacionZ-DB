-- FUNCIONES para TAB_PERFORACIONES
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE PERFORACIONES
CREATE OR REPLACE FUNCTION FUN_VALIDAR_PERFORACIONES(
    jID_PROYECTO TAB_PERFORACIONES.ID_PROYECTO%TYPE,
    jPROFUNDIDAD_ACTUAL TAB_PERFORACIONES.PROFUNDIDAD_ACTUAL%TYPE,
    jFECHA_INICIO_PERFORACION TAB_PERFORACIONES.FECHA_INICIO_PERFORACION%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
BEGIN
    --VALIDACION VARIABLE ID_PROYECTO
    IF jID_PROYECTO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PROYECTO no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_PROYECTOS WHERE ID_PROYECTO = jID_PROYECTO) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PROYECTO no existe en TAB_PROYECTOS'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE PROFUNDIDAD_ACTUAL
    ELSIF jPROFUNDIDAD_ACTUAL IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_ACTUAL no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jPROFUNDIDAD_ACTUAL < 0 THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_ACTUAL no puede ser negativa'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE FECHA_INICIO_PERFORACION
    ELSIF jFECHA_INICIO_PERFORACION IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_INICIO_PERFORACION no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jFECHA_INICIO_PERFORACION > NOW() THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_INICIO_PERFORACION no puede ser en el futuro'); 
        RETURN RETORNO;

    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA VALIDAR LOS DATOS A ACTUALIZAR EN LA TABLA DE PERFORACIONES
CREATE OR REPLACE FUNCTION FUN_VALIDAR_ACTUALIZAR_PERFORACIONES(
    jID_PERFORACION TAB_PERFORACIONES.ID_PERFORACION%TYPE,
    jID_PROYECTO TAB_PERFORACIONES.ID_PROYECTO%TYPE,
    jPROFUNDIDAD_ACTUAL TAB_PERFORACIONES.PROFUNDIDAD_ACTUAL%TYPE,
    jFECHA_INICIO_PERFORACION TAB_PERFORACIONES.FECHA_INICIO_PERFORACION%TYPE,
    jFECHA_FIN_PERFORACION TAB_PERFORACIONES.FECHA_FIN_PERFORACION%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
BEGIN
    --VALIDACION VARIABLE ID_PERFORACION
    IF jID_PERFORACION IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PERFORACION no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_PERFORACIONES WHERE ID_PERFORACION = jID_PERFORACION) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PERFORACION no existe en la tabla'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ID_PROYECTO
    ELSIF jID_PROYECTO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PROYECTO no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_PROYECTOS WHERE ID_PROYECTO = jID_PROYECTO) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PROYECTO no existe en TAB_PROYECTOS'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE PROFUNDIDAD_ACTUAL
    ELSIF jPROFUNDIDAD_ACTUAL IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_ACTUAL no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jPROFUNDIDAD_ACTUAL < 0 THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_ACTUAL no puede ser negativa'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE FECHA_INICIO_PERFORACION
    ELSIF jFECHA_INICIO_PERFORACION IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_INICIO_PERFORACION no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jFECHA_INICIO_PERFORACION > NOW() THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_INICIO_PERFORACION no puede ser en el futuro'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE FECHA_FIN_PERFORACION (es opcional)
    ELSIF jFECHA_FIN_PERFORACION IS NOT NULL AND jFECHA_FIN_PERFORACION < jFECHA_INICIO_PERFORACION THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_FIN_PERFORACION no puede ser menor que FECHA_INICIO_PERFORACION'); 
        RETURN RETORNO;

    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA INSERTAR UNA NUEVA PERFORACION EN LA TABLA DE PERFORACIONES
CREATE OR REPLACE FUNCTION FUN_INSERTAR_PERFORACIONES_JSON(
    jID_PROYECTO TAB_PERFORACIONES.ID_PROYECTO%TYPE,
    jPROFUNDIDAD_ACTUAL TAB_PERFORACIONES.PROFUNDIDAD_ACTUAL%TYPE,
    jFECHA_INICIO_PERFORACION TAB_PERFORACIONES.FECHA_INICIO_PERFORACION%TYPE,
    jUSUARIO_CREACION TAB_PERFORACIONES.USUARIO_CREACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
    jID_PERFORACION INTEGER;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_PERFORACIONES(jID_PROYECTO, jPROFUNDIDAD_ACTUAL, jFECHA_INICIO_PERFORACION);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --GENERAR EL ID DE LA PERFORACION
    jID_PERFORACION := funcion_Retorna_ultmoid('TAB_PERFORACIONES', 'ID_PERFORACION');

    --INSERTAR LA NUEVA PERFORACION EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        INSERT INTO TAB_PERFORACIONES (ID_PERFORACION, ID_PROYECTO, PROFUNDIDAD_ACTUAL, FECHA_INICIO_PERFORACION, USUARIO_CREACION)
        VALUES (jID_PERFORACION, jID_PROYECTO, jPROFUNDIDAD_ACTUAL, jFECHA_INICIO_PERFORACION, jUSUARIO_CREACION);

        --VERIFICAR SI LA INSERCION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Perforación insertada exitosamente', 'ID_PERFORACION', jID_PERFORACION); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar la perforación'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA ACTUALIZAR LOS DATOS DE UNA PERFORACION EN LA TABLA DE PERFORACIONES
CREATE OR REPLACE FUNCTION FUN_ACTUALIZAR_PERFORACIONES_JSON(
    jID_PERFORACION TAB_PERFORACIONES.ID_PERFORACION%TYPE,
    jID_PROYECTO TAB_PERFORACIONES.ID_PROYECTO%TYPE,
    jPROFUNDIDAD_ACTUAL TAB_PERFORACIONES.PROFUNDIDAD_ACTUAL%TYPE,
    jFECHA_INICIO_PERFORACION TAB_PERFORACIONES.FECHA_INICIO_PERFORACION%TYPE,
    jFECHA_FIN_PERFORACION TAB_PERFORACIONES.FECHA_FIN_PERFORACION%TYPE,
    jUSUARIO_MODIFICACION TAB_PERFORACIONES.USUARIO_MODIFICACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_ACTUALIZAR_PERFORACIONES(jID_PERFORACION, jID_PROYECTO, jPROFUNDIDAD_ACTUAL, jFECHA_INICIO_PERFORACION, jFECHA_FIN_PERFORACION);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --ACTUALIZAR LOS DATOS DE LA PERFORACION EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_PERFORACIONES
        SET ID_PROYECTO = jID_PROYECTO,
            PROFUNDIDAD_ACTUAL = jPROFUNDIDAD_ACTUAL,
            FECHA_INICIO_PERFORACION = jFECHA_INICIO_PERFORACION,
            FECHA_FIN_PERFORACION = jFECHA_FIN_PERFORACION,
            USUARIO_MODIFICACION = jUSUARIO_MODIFICACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_PERFORACION = jID_PERFORACION;

        --VERIFICAR SI LA ACTUALIZACION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Perforación actualizada exitosamente'); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar la perforación'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE UNA PERFORACION POR SU ID
CREATE OR REPLACE FUNCTION FUN_OBTENER_PERFORACION_POR_ID_JSON(
    jID_PERFORACION TAB_PERFORACIONES.ID_PERFORACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    DATOS_PERFORACION JSON;
BEGIN
    SELECT json_build_object(
        'ID_PERFORACION', ID_PERFORACION,
        'ID_PROYECTO', ID_PROYECTO,
        'PROFUNDIDAD_ACTUAL', PROFUNDIDAD_ACTUAL,
        'FECHA_INICIO_PERFORACION', FECHA_INICIO_PERFORACION,
        'FECHA_FIN_PERFORACION', FECHA_FIN_PERFORACION
    )
    INTO DATOS_PERFORACION
    FROM TAB_PERFORACIONES
    WHERE ID_PERFORACION = jID_PERFORACION;

    IF DATOS_PERFORACION IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'Perforación no encontrada'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Perforación encontrada', 'DATOS', DATOS_PERFORACION); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE TODAS LAS PERFORACIONES
CREATE OR REPLACE FUNCTION FUN_OBTENER_TODAS_PERFORACIONES_JSON() RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    LISTA_PERFORACIONES JSON;
BEGIN
    SELECT json_agg(json_build_object(
        'ID_PERFORACION', ID_PERFORACION,
        'ID_PROYECTO', ID_PROYECTO,
        'PROFUNDIDAD_ACTUAL', PROFUNDIDAD_ACTUAL,
        'FECHA_INICIO_PERFORACION', FECHA_INICIO_PERFORACION,
        'FECHA_FIN_PERFORACION', FECHA_FIN_PERFORACION
    )) INTO LISTA_PERFORACIONES
    FROM TAB_PERFORACIONES;

    IF LISTA_PERFORACIONES IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'No se encontraron perforaciones'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Perforaciones encontradas', 'DATOS', LISTA_PERFORACIONES); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;
