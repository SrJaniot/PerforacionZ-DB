-- FUNCIONES para TAB_MOVIMIENTO_DE_BROCA
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE MOVIMIENTO_DE_BROCA
CREATE OR REPLACE FUNCTION FUN_VALIDAR_MOVIMIENTO_DE_BROCA(
    jID_BROCA_INSTANCIADA TAB_MOVIMIENTO_DE_BROCA.ID_BROCA_INSTANCIADA%TYPE,
    jID_PERFORACION TAB_MOVIMIENTO_DE_BROCA.ID_PERFORACION%TYPE,
    jFECHA_HORA_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.FECHA_HORA_MOVIMIENTO%TYPE,
    jTIPO_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.TIPO_MOVIMIENTO%TYPE,
    jPROFUNDIDAD_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.PROFUNDIDAD_MOVIMIENTO%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    vTIPOS CONSTANT VARCHAR[] := ARRAY['ENTRADA', 'SALIDA', 'CAMBIO'];
    vULTIMA_PROFUNDIDAD FLOAT;
BEGIN
    --VALIDACION VARIABLE ID_BROCA_INSTANCIADA
    IF jID_BROCA_INSTANCIADA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAINSTANCIADAS WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no existe en TAB_BROCAINSTANCIADAS'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ID_PERFORACION
    ELSIF jID_PERFORACION IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PERFORACION no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_PERFORACIONES WHERE ID_PERFORACION = jID_PERFORACION) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_PERFORACION no existe en TAB_PERFORACIONES'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE FECHA_HORA_MOVIMIENTO
    ELSIF jFECHA_HORA_MOVIMIENTO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_HORA_MOVIMIENTO no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jFECHA_HORA_MOVIMIENTO > NOW() THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_HORA_MOVIMIENTO no puede ser en el futuro'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE TIPO_MOVIMIENTO
    ELSIF jTIPO_MOVIMIENTO IS NULL OR jTIPO_MOVIMIENTO = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'TIPO_MOVIMIENTO no puede ser nulo o vacío'); 
        RETURN RETORNO;
    ELSIF NOT jTIPO_MOVIMIENTO = ANY(vTIPOS) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'TIPO_MOVIMIENTO debe ser: ENTRADA, SALIDA o CAMBIO'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE PROFUNDIDAD_MOVIMIENTO
    ELSIF jPROFUNDIDAD_MOVIMIENTO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_MOVIMIENTO no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jPROFUNDIDAD_MOVIMIENTO < 0 THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_MOVIMIENTO no puede ser negativa'); 
        RETURN RETORNO;

    --VALIDACION DE PROFUNDIDAD PROGRESIVA: la profundidad debe ser >= a la última profundidad registrada para esta broca en esta perforación
    ELSE
        SELECT MAX(PROFUNDIDAD_MOVIMIENTO) INTO vULTIMA_PROFUNDIDAD 
        FROM TAB_MOVIMIENTO_DE_BROCA 
        WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA AND ID_PERFORACION = jID_PERFORACION;

        IF vULTIMA_PROFUNDIDAD IS NOT NULL AND jPROFUNDIDAD_MOVIMIENTO < vULTIMA_PROFUNDIDAD THEN
            RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'PROFUNDIDAD_MOVIMIENTO debe ser mayor o igual a la última profundidad registrada (' || vULTIMA_PROFUNDIDAD || ') para esta broca en esta perforación'); 
            RETURN RETORNO;
        ELSE
            RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
        END IF;
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


