    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --ACTUALIZAR LOS DATOS DEL MOVIMIENTO EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_MOVIMIENTO_DE_BROCA
        SET ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA,
            ID_PERFORACION = jID_PERFORACION,
            FECHA_HORA_MOVIMIENTO = jFECHA_HORA_MOVIMIENTO,
            TIPO_MOVIMIENTO = jTIPO_MOVIMIENTO,
