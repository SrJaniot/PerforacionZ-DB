-- FUNCION PARA INSERTAR UN NUEVO MOVIMIENTO DE BROCA EN LA TABLA
CREATE OR REPLACE FUNCTION FUN_INSERTAR_MOVIMIENTO_DE_BROCA_JSON(
    jID_BROCA_INSTANCIADA TAB_MOVIMIENTO_DE_BROCA.ID_BROCA_INSTANCIADA%TYPE,
    jID_PERFORACION TAB_MOVIMIENTO_DE_BROCA.ID_PERFORACION%TYPE,
    jFECHA_HORA_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.FECHA_HORA_MOVIMIENTO%TYPE,
    jTIPO_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.TIPO_MOVIMIENTO%TYPE,
    jPROFUNDIDAD_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.PROFUNDIDAD_MOVIMIENTO%TYPE,
    jUSUARIO_CREACION TAB_MOVIMIENTO_DE_BROCA.USUARIO_CREACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
    jID_MOVIMIENTO INTEGER;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_MOVIMIENTO_DE_BROCA(jID_BROCA_INSTANCIADA, jID_PERFORACION, jFECHA_HORA_MOVIMIENTO, jTIPO_MOVIMIENTO, jPROFUNDIDAD_MOVIMIENTO);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --GENERAR EL ID DEL MOVIMIENTO
    jID_MOVIMIENTO := funcion_Retorna_ultmoid('TAB_MOVIMIENTO_DE_BROCA', 'ID_MOVIMIENTO');

    --INSERTAR EL NUEVO MOVIMIENTO EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        INSERT INTO TAB_MOVIMIENTO_DE_BROCA (ID_MOVIMIENTO, ID_BROCA_INSTANCIADA, ID_PERFORACION, FECHA_HORA_MOVIMIENTO, TIPO_MOVIMIENTO, PROFUNDIDAD_MOVIMIENTO, USUARIO_CREACION)
        VALUES (jID_MOVIMIENTO, jID_BROCA_INSTANCIADA, jID_PERFORACION, jFECHA_HORA_MOVIMIENTO, jTIPO_MOVIMIENTO, jPROFUNDIDAD_MOVIMIENTO, jUSUARIO_CREACION);

        --ACTUALIZAR LA PROFUNDIDAD_ACTUAL EN TAB_PERFORACIONES
        UPDATE TAB_PERFORACIONES
        SET PROFUNDIDAD_ACTUAL = jPROFUNDIDAD_MOVIMIENTO
        WHERE ID_PERFORACION = jID_PERFORACION;

        --CAMBIAR EL ESTADO DE LA BROCA A EN_USO (si aún no lo está)
        UPDATE TAB_BROCAINSTANCIADAS
        SET ESTADO_BROCA = 'EN_USO',
            FECHA_ULTIMO_USO_BROCA = jFECHA_HORA_MOVIMIENTO,
            USUARIO_MODIFICACION = jUSUARIO_CREACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA AND ESTADO_BROCA = 'NUEVA';

        --VERIFICAR SI LA INSERCION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Movimiento de broca insertado exitosamente', 'ID_MOVIMIENTO', jID_MOVIMIENTO); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar el movimiento de broca'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA ACTUALIZAR LOS DATOS DE UN MOVIMIENTO DE BROCA EN LA TABLA
