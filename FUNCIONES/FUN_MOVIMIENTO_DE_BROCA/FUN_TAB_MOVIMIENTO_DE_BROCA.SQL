-- FUNCION PARA OBTENER LOS DATOS DE UN MOVIMIENTO DE BROCA POR SU ID
CREATE OR REPLACE FUNCTION FUN_OBTENER_MOVIMIENTO_DE_BROCA_POR_ID_JSON(
    jID_MOVIMIENTO TAB_MOVIMIENTO_DE_BROCA.ID_MOVIMIENTO%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    DATOS_MOVIMIENTO JSON;
BEGIN
    SELECT json_build_object(
        'ID_MOVIMIENTO', ID_MOVIMIENTO,
        'ID_BROCA_INSTANCIADA', ID_BROCA_INSTANCIADA,
        'ID_PERFORACION', ID_PERFORACION,
        'FECHA_HORA_MOVIMIENTO', FECHA_HORA_MOVIMIENTO,
        'TIPO_MOVIMIENTO', TIPO_MOVIMIENTO,
        'PROFUNDIDAD_MOVIMIENTO', PROFUNDIDAD_MOVIMIENTO
    )
    INTO DATOS_MOVIMIENTO
    FROM TAB_MOVIMIENTO_DE_BROCA
    WHERE ID_MOVIMIENTO = jID_MOVIMIENTO;

    IF DATOS_MOVIMIENTO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'Movimiento de broca no encontrado'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Movimiento de broca encontrado', 'DATOS', DATOS_MOVIMIENTO); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


