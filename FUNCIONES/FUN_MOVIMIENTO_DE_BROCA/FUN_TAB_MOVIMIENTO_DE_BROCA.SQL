        --ACTUALIZAR LA PROFUNDIDAD_ACTUAL EN TAB_PERFORACIONES
        UPDATE TAB_PERFORACIONES
        SET PROFUNDIDAD_ACTUAL = jPROFUNDIDAD_MOVIMIENTO
        WHERE ID_PERFORACION = jID_PERFORACION;

        --VERIFICAR SI LA ACTUALIZACION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Movimiento de broca actualizado exitosamente'); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar el movimiento de broca'); 
