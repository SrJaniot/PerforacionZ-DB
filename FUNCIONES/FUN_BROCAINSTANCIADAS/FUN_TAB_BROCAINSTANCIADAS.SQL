-- FUNCIONES para TAB_BROCAINSTANCIADAS
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE BROCAINSTANCIADAS
CREATE OR REPLACE FUNCTION FUN_VALIDAR_BROCAINSTANCIADAS(
    jID_BROCA TAB_BROCAINSTANCIADAS.ID_BROCA%TYPE,
    jESTADO_BROCA TAB_BROCAINSTANCIADAS.ESTADO_BROCA%TYPE,
    jFECHA_REGISTRO_BROCA TAB_BROCAINSTANCIADAS.FECHA_REGISTRO_BROCA%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    vESTADOS CONSTANT VARCHAR[] := ARRAY['NUEVA', 'EN_USO', 'DESGASTADA', 'DESCARTADA'];
BEGIN
    --VALIDACION VARIABLE ID_BROCA
    IF jID_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAS WHERE ID_BROCA = jID_BROCA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no existe en TAB_BROCAS'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ESTADO_BROCA
    ELSIF jESTADO_BROCA IS NULL OR jESTADO_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ESTADO_BROCA no puede ser nulo o vacío'); 
        RETURN RETORNO;
    ELSIF NOT jESTADO_BROCA = ANY(vESTADOS) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ESTADO_BROCA debe ser: NUEVA, EN_USO, DESGASTADA o DESCARTADA'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE FECHA_REGISTRO_BROCA
    ELSIF jFECHA_REGISTRO_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_REGISTRO_BROCA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jFECHA_REGISTRO_BROCA > NOW() THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_REGISTRO_BROCA no puede ser en el futuro'); 
        RETURN RETORNO;

    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA VALIDAR LOS DATOS A ACTUALIZAR EN LA TABLA DE BROCAINSTANCIADAS
CREATE OR REPLACE FUNCTION FUN_VALIDAR_ACTUALIZAR_BROCAINSTANCIADAS(
    jID_BROCA_INSTANCIADA TAB_BROCAINSTANCIADAS.ID_BROCA_INSTANCIADA%TYPE,
    jID_BROCA TAB_BROCAINSTANCIADAS.ID_BROCA%TYPE,
    jESTADO_BROCA TAB_BROCAINSTANCIADAS.ESTADO_BROCA%TYPE,
    jFECHA_ULTIMO_USO_BROCA TAB_BROCAINSTANCIADAS.FECHA_ULTIMO_USO_BROCA%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    vESTADOS CONSTANT VARCHAR[] := ARRAY['NUEVA', 'EN_USO', 'DESGASTADA', 'DESCARTADA'];
    vESTADO_ACTUAL VARCHAR;
    vFECHA_REGISTRO TIMESTAMP;
    vTRANSICION_VALIDA BOOLEAN;
BEGIN
    --VALIDACION VARIABLE ID_BROCA_INSTANCIADA
    IF jID_BROCA_INSTANCIADA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAINSTANCIADAS WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no existe en la tabla'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ID_BROCA: NO se puede cambiar (es inmutable, representa la broca física)
    ELSIF jID_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAS WHERE ID_BROCA = jID_BROCA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no existe en TAB_BROCAS'); 
        RETURN RETORNO;
    ELSIF (SELECT ID_BROCA FROM TAB_BROCAINSTANCIADAS WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA) != jID_BROCA THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no puede ser modificado. Una broca instanciada es una broca física específica'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ESTADO_BROCA
    ELSIF jESTADO_BROCA IS NULL OR jESTADO_BROCA = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ESTADO_BROCA no puede ser nulo o vacío'); 
        RETURN RETORNO;
    ELSIF NOT jESTADO_BROCA = ANY(vESTADOS) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ESTADO_BROCA debe ser: NUEVA, EN_USO, DESGASTADA o DESCARTADA'); 
        RETURN RETORNO;

    ELSE
        -- VALIDACION DE TRANSICION DE ESTADO: debe ser progresivo (NUEVA -> EN_USO -> DESGASTADA -> DESCARTADA)
        SELECT ESTADO_BROCA, FECHA_REGISTRO_BROCA INTO vESTADO_ACTUAL, vFECHA_REGISTRO
        FROM TAB_BROCAINSTANCIADAS 
        WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA;

        -- Validar transiciones válidas
        vTRANSICION_VALIDA := (
            (vESTADO_ACTUAL = 'NUEVA' AND jESTADO_BROCA = 'EN_USO') OR
            (vESTADO_ACTUAL = 'EN_USO' AND jESTADO_BROCA = 'DESGASTADA') OR
            (vESTADO_ACTUAL = 'DESGASTADA' AND jESTADO_BROCA = 'DESCARTADA') OR
            (vESTADO_ACTUAL = jESTADO_BROCA) -- Permite mantener el mismo estado
        );

        IF NOT vTRANSICION_VALIDA THEN
            RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'Transición de estado inválida. Estado actual: ' || vESTADO_ACTUAL || '. Las transiciones válidas son: NUEVA->EN_USO->DESGASTADA->DESCARTADA'); 
            RETURN RETORNO;
        END IF;

        --VALIDACION VARIABLE FECHA_ULTIMO_USO_BROCA (opcional)
        IF jFECHA_ULTIMO_USO_BROCA IS NOT NULL AND jFECHA_ULTIMO_USO_BROCA < vFECHA_REGISTRO THEN
            RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_ULTIMO_USO_BROCA no puede ser menor que FECHA_REGISTRO_BROCA'); 
            RETURN RETORNO;
        END IF;

        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos para actualizar');
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA INSERTAR UNA NUEVA BROCA INSTANCIADA EN LA TABLA
CREATE OR REPLACE FUNCTION FUN_INSERTAR_BROCAINSTANCIADAS_JSON(
    jID_BROCA_INSTANCIADA TAB_BROCAINSTANCIADAS.ID_BROCA_INSTANCIADA%TYPE,
    jID_BROCA TAB_BROCAINSTANCIADAS.ID_BROCA%TYPE,
    jFECHA_REGISTRO_BROCA TAB_BROCAINSTANCIADAS.FECHA_REGISTRO_BROCA%TYPE,
    jUSUARIO_CREACION TAB_BROCAINSTANCIADAS.USUARIO_CREACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jESTADO_BROCA CONSTANT VARCHAR := 'NUEVA';
BEGIN
    --VALIDAR ID_BROCA_INSTANCIADA no sea nulo
    IF jID_BROCA_INSTANCIADA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA no puede ser nulo'); 
        RETURN RETORNO;
    END IF;

    --VALIDAR ID_BROCA_INSTANCIADA sea único
    IF EXISTS (SELECT 1 FROM TAB_BROCAINSTANCIADAS WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA_INSTANCIADA ya existe en la tabla'); 
        RETURN RETORNO;
    END IF;

    --VALIDAR ID_BROCA existe
    IF jID_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_BROCAS WHERE ID_BROCA = jID_BROCA) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_BROCA no existe en TAB_BROCAS'); 
        RETURN RETORNO;
    END IF;

    --VALIDAR FECHA_REGISTRO_BROCA
    IF jFECHA_REGISTRO_BROCA IS NULL THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_REGISTRO_BROCA no puede ser nulo'); 
        RETURN RETORNO;
    ELSIF jFECHA_REGISTRO_BROCA > NOW() THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'FECHA_REGISTRO_BROCA no puede ser en el futuro'); 
        RETURN RETORNO;
    END IF;

    --INSERTAR LA NUEVA BROCA INSTANCIADA EN LA TABLA (SIEMPRE con estado NUEVA)
    INSERT INTO TAB_BROCAINSTANCIADAS (ID_BROCA_INSTANCIADA, ID_BROCA, ESTADO_BROCA, FECHA_REGISTRO_BROCA, USUARIO_CREACION)
    VALUES (jID_BROCA_INSTANCIADA, jID_BROCA, jESTADO_BROCA, jFECHA_REGISTRO_BROCA, jUSUARIO_CREACION);

    --VERIFICAR SI LA INSERCION FUE EXITOSA
    IF FOUND THEN
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Broca instanciada insertada exitosamente con estado NUEVA', 'ID_BROCA_INSTANCIADA', jID_BROCA_INSTANCIADA); 
    ELSE
        RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar la broca instanciada'); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA ACTUALIZAR LOS DATOS DE UNA BROCA INSTANCIADA
CREATE OR REPLACE FUNCTION FUN_ACTUALIZAR_BROCAINSTANCIADAS_JSON(
    jID_BROCA_INSTANCIADA TAB_BROCAINSTANCIADAS.ID_BROCA_INSTANCIADA%TYPE,
    jID_BROCA TAB_BROCAINSTANCIADAS.ID_BROCA%TYPE,
    jESTADO_BROCA TAB_BROCAINSTANCIADAS.ESTADO_BROCA%TYPE,
    jFECHA_ULTIMO_USO_BROCA TAB_BROCAINSTANCIADAS.FECHA_ULTIMO_USO_BROCA%TYPE,
    jUSUARIO_MODIFICACION TAB_BROCAINSTANCIADAS.USUARIO_MODIFICACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_ACTUALIZAR_BROCAINSTANCIADAS(jID_BROCA_INSTANCIADA, jID_BROCA, jESTADO_BROCA, jFECHA_ULTIMO_USO_BROCA);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --ACTUALIZAR LOS DATOS DE LA BROCA INSTANCIADA EN LA TABLA (sin cambiar ID_BROCA ni FECHA_REGISTRO_BROCA)
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_BROCAINSTANCIADAS
        SET ESTADO_BROCA = jESTADO_BROCA,
            FECHA_ULTIMO_USO_BROCA = jFECHA_ULTIMO_USO_BROCA,
            USUARIO_MODIFICACION = jUSUARIO_MODIFICACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_BROCA_INSTANCIADA = jID_BROCA_INSTANCIADA;

        --VERIFICAR SI LA ACTUALIZACION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Broca instanciada actualizada exitosamente'); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar la broca instanciada'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


