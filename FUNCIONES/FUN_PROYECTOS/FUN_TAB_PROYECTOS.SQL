-- FUNCIONES para TAB_PROYECTOS
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE PROYECTOS
CREATE OR REPLACE FUNCTION FUN_VALIDAR_PROYECTOS(
    jID_SUPERVISOR TAB_PROYECTOS.ID_SUPERVISOR%TYPE,
    jNOM_PROYECTO TAB_PROYECTOS.NOM_PROYECTO%TYPE,
    jID_MUNICIPIO TAB_PROYECTOS.ID_MUNICIPIO%TYPE,
    jDESCRIPCION_PROYECTO TAB_PROYECTOS.DESCRIPCION_PROYECTO%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
BEGIN
    --VALIDACION VARIABLE ID_SUPERVISOR
    IF jID_SUPERVISOR IS NULL OR jID_SUPERVISOR = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_SUPERVISOR no puede ser nulo o vacío'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_SUPERVISORES WHERE ID_SUPERVISOR = jID_SUPERVISOR) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_SUPERVISOR no existe en TAB_SUPERVISORES'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE NOM_PROYECTO
    ELSIF jNOM_PROYECTO IS NULL OR jNOM_PROYECTO = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'NOM_PROYECTO no puede ser nulo o vacío'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE ID_MUNICIPIO
    ELSIF jID_MUNICIPIO IS NULL OR jID_MUNICIPIO = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_MUNICIPIO no puede ser nulo o vacío'); 
        RETURN RETORNO;
    ELSIF NOT EXISTS (SELECT 1 FROM TAB_MUNICIPIOS WHERE ID_MUNICIPIO = jID_MUNICIPIO) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_MUNICIPIO no existe en TAB_MUNICIPIOS'); 
        RETURN RETORNO;

    --VALIDACION VARIABLE DESCRIPCION_PROYECTO
    ELSIF jDESCRIPCION_PROYECTO IS NULL OR jDESCRIPCION_PROYECTO = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'DESCRIPCION_PROYECTO no puede ser nulo o vacío'); 
        RETURN RETORNO;

    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos');
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


