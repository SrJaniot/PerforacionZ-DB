-- FUNCION PARA INSERTAR UN NUEVO PROYECTO EN LA TABLA DE PROYECTOS
CREATE OR REPLACE FUNCTION FUN_INSERTAR_PROYECTOS_JSON(
    jID_SUPERVISOR TAB_PROYECTOS.ID_SUPERVISOR%TYPE,
    jNOM_PROYECTO TAB_PROYECTOS.NOM_PROYECTO%TYPE,
    jID_MUNICIPIO TAB_PROYECTOS.ID_MUNICIPIO%TYPE,
    jDESCRIPCION_PROYECTO TAB_PROYECTOS.DESCRIPCION_PROYECTO%TYPE,
    jUSUARIO_CREACION TAB_PROYECTOS.USUARIO_CREACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
    jID_PROYECTO INTEGER;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_PROYECTOS(jID_SUPERVISOR, jNOM_PROYECTO, jID_MUNICIPIO, jDESCRIPCION_PROYECTO);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --GENERAR EL ID DEL PROYECTO
    jID_PROYECTO := funcion_Retorna_ultmoid('TAB_PROYECTOS', 'ID_PROYECTO');

    --INSERTAR EL NUEVO PROYECTO EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        INSERT INTO TAB_PROYECTOS (ID_PROYECTO, ID_SUPERVISOR, NOM_PROYECTO, ID_MUNICIPIO, DESCRIPCION_PROYECTO, USUARIO_CREACION)
        VALUES (jID_PROYECTO, jID_SUPERVISOR, jNOM_PROYECTO, jID_MUNICIPIO, jDESCRIPCION_PROYECTO, jUSUARIO_CREACION);

        --VERIFICAR SI LA INSERCION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Proyecto insertado exitosamente', 'ID_PROYECTO', jID_PROYECTO); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar el proyecto'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA ACTUALIZAR LOS DATOS DE UN PROYECTO EN LA TABLA DE PROYECTOS
CREATE OR REPLACE FUNCTION FUN_ACTUALIZAR_PROYECTOS_JSON(
    jID_PROYECTO TAB_PROYECTOS.ID_PROYECTO%TYPE,
    jID_SUPERVISOR TAB_PROYECTOS.ID_SUPERVISOR%TYPE,
    jNOM_PROYECTO TAB_PROYECTOS.NOM_PROYECTO%TYPE,
    jID_MUNICIPIO TAB_PROYECTOS.ID_MUNICIPIO%TYPE,
    jDESCRIPCION_PROYECTO TAB_PROYECTOS.DESCRIPCION_PROYECTO%TYPE,
    jUSUARIO_MODIFICACION TAB_PROYECTOS.USUARIO_MODIFICACION%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_ACTUALIZAR_PROYECTOS(jID_PROYECTO, jID_SUPERVISOR, jNOM_PROYECTO, jID_MUNICIPIO, jDESCRIPCION_PROYECTO);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION;
    END IF;

    --ACTUALIZAR LOS DATOS DEL PROYECTO EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_PROYECTOS
        SET ID_SUPERVISOR = jID_SUPERVISOR,
            NOM_PROYECTO = jNOM_PROYECTO,
            ID_MUNICIPIO = jID_MUNICIPIO,
            DESCRIPCION_PROYECTO = jDESCRIPCION_PROYECTO,
            USUARIO_MODIFICACION = jUSUARIO_MODIFICACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_PROYECTO = jID_PROYECTO;

        --VERIFICAR SI LA ACTUALIZACION FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Proyecto actualizado exitosamente'); 
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar el proyecto'); 
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE UN PROYECTO POR SU ID
CREATE OR REPLACE FUNCTION FUN_OBTENER_PROYECTO_POR_ID_JSON(
    jID_PROYECTO TAB_PROYECTOS.ID_PROYECTO%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    DATOS_PROYECTO JSON;
BEGIN
    SELECT json_build_object(
        'ID_PROYECTO', ID_PROYECTO,
        'ID_SUPERVISOR', ID_SUPERVISOR,
        'NOM_PROYECTO', NOM_PROYECTO,
        'ID_MUNICIPIO', ID_MUNICIPIO,
        'DESCRIPCION_PROYECTO', DESCRIPCION_PROYECTO
    )
    INTO DATOS_PROYECTO
    FROM TAB_PROYECTOS
    WHERE ID_PROYECTO = jID_PROYECTO;

    IF DATOS_PROYECTO IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'Proyecto no encontrado'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Proyecto encontrado', 'DATOS', DATOS_PROYECTO); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE TODOS LOS PROYECTOS
CREATE OR REPLACE FUNCTION FUN_OBTENER_TODOS_PROYECTOS_JSON() RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    LISTA_PROYECTOS JSON;
BEGIN
    SELECT json_agg(json_build_object(
        'ID_PROYECTO', ID_PROYECTO,
        'ID_SUPERVISOR', ID_SUPERVISOR,
        'NOM_PROYECTO', NOM_PROYECTO,
        'ID_MUNICIPIO', ID_MUNICIPIO,
        'DESCRIPCION_PROYECTO', DESCRIPCION_PROYECTO
    )) INTO LISTA_PROYECTOS
    FROM TAB_PROYECTOS;

    IF LISTA_PROYECTOS IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'No se encontraron proyectos'); 
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Proyectos encontrados', 'DATOS', LISTA_PROYECTOS); 
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;
