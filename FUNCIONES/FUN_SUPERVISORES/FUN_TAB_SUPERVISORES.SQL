-- FUNCION PARA INSERTAR UN NUEVO SUPERVISOR EN LA TABLA DE SUPERVISORES
CREATE OR REPLACE FUNCTION FUN_INSERTAR_SUPERVISORES_JSON(
    jID_SUPERVISOR TAB_SUPERVISORES.ID_SUPERVISOR%TYPE,
    jNOM_SUPERVISOR TAB_SUPERVISORES.NOM_SUPERVISOR%TYPE,
    jCORREO_SUPERVISOR TAB_SUPERVISORES.CORREO_SUPERVISOR%TYPE,
    jNUEM_CEL_SUPERVISOR TAB_SUPERVISORES.NUEM_CEL_SUPERVISOR%TYPE,
    jUSUARIO_CREACION TAB_SUPERVISORES.USUARIO_CREACION%TYPE,
    jFOTO_PERFIL_SUPERVISOR TAB_SUPERVISORES.FOTO_PERFIL_SUPERVISOR%TYPE DEFAULT 'default_profile.jpg'
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_SUPERVISORES(jID_SUPERVISOR, jNOM_SUPERVISOR, jCORREO_SUPERVISOR, jNUEM_CEL_SUPERVISOR);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION; -- Si la validación no es exitosa, retornar el mensaje de error
    END IF;

    --INSERTAR EL NUEVO SUPERVISOR EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        INSERT INTO TAB_SUPERVISORES (ID_SUPERVISOR, NOM_SUPERVISOR, CORREO_SUPERVISOR, NUEM_CEL_SUPERVISOR, FOTO_PERFIL_SUPERVISOR, USUARIO_CREACION)
        VALUES (jID_SUPERVISOR, jNOM_SUPERVISOR, jCORREO_SUPERVISOR, jNUEM_CEL_SUPERVISOR, jFOTO_PERFIL_SUPERVISOR, jUSUARIO_CREACION);

        --VERIFICAR SI LA INSERCIÓN FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Supervisor insertado exitosamente'); -- Supervisor insertado exitosamente
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al insertar el supervisor'); -- Error al insertar el supervisor
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;



-- FUNCION PARA ACTUALIZAR LOS DATOS DE UN SUPERVISOR EN LA TABLA DE SUPERVISORES
CREATE OR REPLACE FUNCTION FUN_ACTUALIZAR_SUPERVISORES_JSON(
    jID_SUPERVISOR TAB_SUPERVISORES.ID_SUPERVISOR%TYPE,
    jNOM_SUPERVISOR TAB_SUPERVISORES.NOM_SUPERVISOR%TYPE,
    jCORREO_SUPERVISOR TAB_SUPERVISORES.CORREO_SUPERVISOR%TYPE,
    jNUEM_CEL_SUPERVISOR TAB_SUPERVISORES.NUEM_CEL_SUPERVISOR%TYPE,
    jUSUARIO_MODIFICACION TAB_SUPERVISORES.USUARIO_MODIFICACION%TYPE,
    jFOTO_PERFIL_SUPERVISOR TAB_SUPERVISORES.FOTO_PERFIL_SUPERVISOR%TYPE DEFAULT NULL
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    jVALIDACION JSON;
BEGIN
    --LLAMAR A LA FUNCION DE VALIDACION
    jVALIDACION := FUN_VALIDAR_ACTUALIZAR_SUPERVISORES(jID_SUPERVISOR, jNOM_SUPERVISOR, jCORREO_SUPERVISOR, jNUEM_CEL_SUPERVISOR);

    --VERIFICAR EL RESULTADO DE LA VALIDACION
    IF (jVALIDACION ->> 'CODIGO')::INTEGER != 200 THEN
        RETURN jVALIDACION; -- Si la validación no es exitosa, retornar el mensaje de error
    END IF;

    --ACTUALIZAR LOS DATOS DEL SUPERVISOR EN LA TABLA
    IF jVALIDACION ->> 'CODIGO' = '200' THEN
        UPDATE TAB_SUPERVISORES
        SET NOM_SUPERVISOR = jNOM_SUPERVISOR,
            CORREO_SUPERVISOR = jCORREO_SUPERVISOR,
            NUEM_CEL_SUPERVISOR = jNUEM_CEL_SUPERVISOR,
            FOTO_PERFIL_SUPERVISOR = COALESCE(jFOTO_PERFIL_SUPERVISOR, FOTO_PERFIL_SUPERVISOR),
            USUARIO_MODIFICACION = jUSUARIO_MODIFICACION,
            FECHA_MODIFICACION = NOW()
        WHERE ID_SUPERVISOR = jID_SUPERVISOR;

        --VERIFICAR SI LA ACTUALIZACIÓN FUE EXITOSA
            IF FOUND THEN
                RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Supervisor actualizado exitosamente'); -- Supervisor actualizado exitosamente
            ELSE
                RETORNO := json_build_object('CODIGO', 500, 'MENSAJE', 'Error al actualizar el supervisor'); -- Error al actualizar el supervisor
            END IF;        
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;



-- FUNCION PARA OBTENER LOS DATOS DE UN SUPERVISOR POR SU ID
CREATE OR REPLACE FUNCTION FUN_OBTENER_SUPERVISOR_POR_ID_JSON(
    jID_SUPERVISOR TAB_SUPERVISORES.ID_SUPERVISOR%TYPE
) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    DATOS_SUPERVISOR JSON;
BEGIN
    SELECT json_build_object(
        'ID_SUPERVISOR', ID_SUPERVISOR,
        'NOM_SUPERVISOR', NOM_SUPERVISOR,
        'CORREO_SUPERVISOR', CORREO_SUPERVISOR,
        'NUEM_CEL_SUPERVISOR', NUEM_CEL_SUPERVISOR,
        'FOTO_PERFIL_SUPERVISOR', FOTO_PERFIL_SUPERVISOR
    )
    INTO DATOS_SUPERVISOR
    FROM TAB_SUPERVISORES
    WHERE ID_SUPERVISOR = jID_SUPERVISOR;

    IF DATOS_SUPERVISOR IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'Supervisor no encontrado'); -- Supervisor no encontrado
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Supervisor encontrado', 'DATOS', DATOS_SUPERVISOR); -- Supervisor encontrado
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


-- FUNCION PARA OBTENER LOS DATOS DE TODOS LOS SUPERVISORES
CREATE OR REPLACE FUNCTION FUN_OBTENER_TODOS_SUPERVISORES_JSON() RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
    LISTA_SUPERVISORES JSON;
BEGIN
    SELECT json_agg(json_build_object(
        'ID_SUPERVISOR', ID_SUPERVISOR,
        'NOM_SUPERVISOR', NOM_SUPERVISOR,
        'CORREO_SUPERVISOR', CORREO_SUPERVISOR,
        'NUEM_CEL_SUPERVISOR', NUEM_CEL_SUPERVISOR,
        'FOTO_PERFIL_SUPERVISOR', FOTO_PERFIL_SUPERVISOR
    )) INTO LISTA_SUPERVISORES
    FROM TAB_SUPERVISORES;

    IF LISTA_SUPERVISORES IS NULL THEN
        RETORNO := json_build_object('CODIGO', 404, 'MENSAJE', 'No se encontraron supervisores'); -- No se encontraron supervisores
    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Supervisores encontrados', 'DATOS', LISTA_SUPERVISORES); -- Supervisores encontrados
    END IF;

    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;









