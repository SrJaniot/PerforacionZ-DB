-- FUNCIONES para TAB_SUPERVISORES
--FUNCION PARA VALIDAR LOS DATOS A INSERTAR EN LA TABLA DE SUPERVISORES
CREATE OR REPLACE FUNCTION FUN_VALIDAR_SUPERVISORES(
    jID_SUPERVISOR TAB_SUPERVISORES.ID_SUPERVISOR%TYPE,
    jNOM_SUPERVISOR TAB_SUPERVISORES.NOM_SUPERVISOR%TYPE,
    jCORREO_SUPERVISOR TAB_SUPERVISORES.CORREO_SUPERVISOR%TYPE,
    jNUEM_CEL_SUPERVISOR TAB_SUPERVISORES.NUEM_CEL_SUPERVISOR%TYPE
    ) RETURNS JSON AS
$$
DECLARE
    RETORNO JSON;
BEGIN
    --VALIDACION VARIABLE ID_SUPERVISOR
    IF jID_SUPERVISOR IS NULL OR jID_SUPERVISOR = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_SUPERVISOR no puede ser nulo o vacío'); -- ID_SUPERVISOR no puede ser nulo o vacío
        RETURN RETORNO;
    ELSIF EXISTS (SELECT 1 FROM TAB_SUPERVISORES WHERE ID_SUPERVISOR = jID_SUPERVISOR) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'ID_SUPERVISOR ya existe en la tabla'); -- ID_SUPERVISOR ya existe en la tabla
        RETURN RETORNO;

    --VALIDACION VARIABLE NOM_SUPERVISOR
    ELSIF jNOM_SUPERVISOR IS NULL OR jNOM_SUPERVISOR = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'NOM_SUPERVISOR no puede ser nulo o vacío'); -- NOM_SUPERVISOR no puede ser nulo o vacío
        RETURN RETORNO;

    --VALIDACION VARIABLE CORREO_SUPERVISOR
    ELSIF jCORREO_SUPERVISOR IS NULL OR jCORREO_SUPERVISOR = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'CORREO_SUPERVISOR no puede ser nulo o vacío'); -- CORREO_SUPERVISOR no puede ser nulo o vacío
        RETURN RETORNO;
    ELSIF NOT jCORREO_SUPERVISOR ~* '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'CORREO_SUPERVISOR no tiene un formato válido'); -- CORREO_SUPERVISOR no tiene un formato válido
        RETURN RETORNO;
    ELSEIF EXISTS (SELECT 1 FROM TAB_SUPERVISORES WHERE CORREO_SUPERVISOR = jCORREO_SUPERVISOR) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'CORREO_SUPERVISOR ya existe en la tabla'); -- CORREO_SUPERVISOR ya existe en la tabla
        RETURN RETORNO;

    --VALIDACION VARIABLE NUEM_CEL_SUPERVISOR
    ELSIF jNUEM_CEL_SUPERVISOR IS NULL OR jNUEM_CEL_SUPERVISOR = '' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'NUEM_CEL_SUPERVISOR no puede ser nulo o vacío'); -- NUEM_CEL_SUPERVISOR no puede ser nulo o vacío
        RETURN RETORNO;
    ELSIF NOT jNUEM_CEL_SUPERVISOR ~ '^\d{10}$' THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'NUEM_CEL_SUPERVISOR debe contener exactamente 10 dígitos'); -- NUEM_CEL_SUPERVISOR debe contener exactamente 10 dígitos
        RETURN RETORNO;
    ELSEIF EXISTS (SELECT 1 FROM TAB_SUPERVISORES WHERE NUEM_CEL_SUPERVISOR = jNUEM_CEL_SUPERVISOR) THEN
        RETORNO := json_build_object('CODIGO', 400, 'MENSAJE', 'NUEM_CEL_SUPERVISOR ya existe en la tabla'); -- NUEM_CEL_SUPERVISOR ya existe en la tabla
        RETURN RETORNO;

    ELSE
        RETORNO := json_build_object('CODIGO', 200, 'MENSAJE', 'Todos los datos son válidos'); --Todos los datos son válidos
    END IF;
    RETURN RETORNO;
END;
$$ LANGUAGE plpgsql;


